#include "iter.hpp"

int	main(void)
{
	int		intArray[3] = {42, 420, 4200};
	std::string	strArray[4] = {"cpp07", "made", "by", "rverona-"};
	double		dblArray[4] = {4.2, 42.42, 420.0, 420.420};

	std::cout << "Iterating on int array:" << std::endl;
	::iter<int>(intArray, 3, ::print);

	std::cout << "Iterating on string array:" << std::endl;
	::iter<std::string>(strArray, 4, ::print);

	std::cout << "Iterating on double array:" << std::endl;
	::iter<double>(dblArray, 4, ::print);
}
